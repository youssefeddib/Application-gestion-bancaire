public class client {

    private int id;
    private String nom;
    private String prenom;
    private String email;
    private String telephone;

    public client(int id, String nom, String prenom, String email, String telephone) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.telephone = telephone;
    }

    public int getId (){
        return id;
    }
    public void setId(){
        this.id = id;
    }




}





